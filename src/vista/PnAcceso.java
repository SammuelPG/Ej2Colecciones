    private void txtNombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            txtContrasena.requestFocus();
        }
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            txtNombre.setText("");
        }
    }//GEN-LAST:event_txtNombreKeyPressed

    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        String login = txtNombre.getText();
        String contra = new String(txtContrasena.getPassword());
        if (empresa.buscarUsuario(login, contra)) {
            new VntOperaciones(empresa).setVisible(true);
            dispose();
            pack();
        } else {
            JOptionPane.showMessageDialog(this, "Usuario inexistente", "Error", JOptionPane.ERROR_MESSAGE);
            limpiar();
        }
    }//GEN-LAST:event_btAceptarActionPerformed

    private void btCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCancelarActionPerformed
        txtNombre.setText("");
        txtContrasena.setText("");
    }//GEN-LAST:event_btCancelarActionPerformed

    private void txtContrasenaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContrasenaKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            btAceptar.doClick();
        }
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            txtContrasena.setText("");
        }
    }//GEN-LAST:event_txtContrasenaKeyPressed
 private void limpiar() {
        txtNombre.setText("");
        txtContrasena.setText("");
    }
