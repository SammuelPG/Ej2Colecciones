    private void cargarNoticias(ArrayList<Noticia> noticia) {
        for (Noticia n : noticia) {
            Vector v = new Vector();
            v.add(n.getCodigoNot());
            v.add(n.getTitulo());
            modelo.addRow(v);
        }
    }

