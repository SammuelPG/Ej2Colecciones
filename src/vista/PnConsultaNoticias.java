    private void btBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBorrarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        int pos = empresa.BuscarunaNoticia(codigo);
        empresa.borrarNoticia(pos);
        modelo.removeRow(fila);
        limpiar();
    }//GEN-LAST:event_btBorrarActionPerformed

    private void tbNoticiasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbNoticiasMouseClicked
        fila = tbNoticias.rowAtPoint(evt.getPoint());
        if (fila != -1) {
            int codigo = Integer.parseInt(tbNoticias.getValueAt(fila, 0).toString());
            Noticia noticia = empresa.unaNoticia(codigo);
            if (noticia != null) {
                txtCodigo.setText(Integer.toString(noticia.getCodigoNot()));
                txtAutor.setText(noticia.getUsuario().getNombre());
                txtCategoria.setText(noticia.getCategoria().toString());
                txtFecha.setText(noticia.getFecha().devolverFecha());
                txtTitulo.setText(noticia.getTitulo());
            }
        }
    }//GEN-LAST:event_tbNoticiasMouseClicked
    public void limpiar(){
        txtCodigo.setText("");
        txtAutor.setText("");
        txtTitulo.setText("");
        txtFecha.setText("");
        txtCategoria.setText("");
    }
