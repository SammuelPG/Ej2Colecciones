    public Fecha pedirFecha(String mensaje) {
        Fecha miFecha = new Fecha();
        if(!(miFecha.comprobarFecha(mensaje))) {
           JOptionPane.showMessageDialog(this, "Fecha incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
           txtFecha.setText("");
           txtFecha.requestFocus();
        }
        return miFecha;
    }

